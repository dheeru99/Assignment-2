    private void searchEmp(String str){
            DefaultTableModel tbl= (DefaultTableModel)table.getModel();
            TableRowSorter<DefaultTableModel> search =new TableRowSorter<>(tbl);
            table.setRowSorter(search);
            search.setRowFilter(RowFilter.regexFilter(str));
            
    }
