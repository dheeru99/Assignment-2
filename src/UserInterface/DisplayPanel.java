    private void table(){
        DefaultTableModel tbl = (DefaultTableModel)table.getModel();
        tbl.setRowCount(0);
        for(Information info: prevInfo.getPrevInfo()){
            Object[] row = new Object[20];
            row[0]= info.getName();
            row[1]=info.getEmpId();
            row[2]=info.getAge();
            row[3]=info.getGender();
            row[4]=info.getStartDate();
            row[5]=info.getLevel();        
            row[6]=info.getTeamInfo();        
            row[7]=info.getPositionTitle();        
            row[8]=info.getPhoneNumber();        
            row[9]=info;
            row[10]=info.getPhoto();
            tbl.addRow(row);
            
        }
        
    }

